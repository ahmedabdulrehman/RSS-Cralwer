package rss.input;

/*
 * Represents one URI object
 */
public class uriObject {

    String channel;
    String link;


    public String getChannel() {
        return channel;
    }

    public void setChannel(String channel) {
        this.channel = channel;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }
}